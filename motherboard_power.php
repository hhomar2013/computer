              <h2>شقوق الذاكرة العشوائية RAM slots</h2>
              <p>تتميز بلونها الأسود في حالة عدم وجود خاصية " Dual Channel " ووجود قفلين باللون الأبيض علي أجنابها واذا كانت اللوحة بها خاصية " Dual Channel " فأن شقوق الذاكرة سيكون لها لونين مختلفين ، تختلف حسب نوع الذاكرة المستخدمة ، تتكون الذاكرة من 4 أنواع وهي SDRAMSDRAM , DDR-SDRAM , RDRAM وأخير ذاكرة DDR2 و DDR3 .
              توقفت الشركات عن أنتاج لوحات مذربورد تدعم الذاكرة SDRAM أما RDRAM ما زالت بعض الشركات تنتجها ولكن علي نطاق ضيق ، تكون أنواع الذاكرة غير متوافقة مع بعضها ولذا لا يمكن تركيب أكثر من نوع ولا يمكن تركيب نوع بشق مصمم لنوع أخر .<br/>
