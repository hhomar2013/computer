              <h2>البيوس BIOS</h2>
              <p>رمز BIOS هو اختصار لمصطلح Basic Input Output System وهي تعنى النظام (البرنامج)
              الأساسي لدخول وخروج المعلومة، هذا البرنامج مسئول عن أساسيات عمل الحاسب، أمور مثل التحكم بشريحتي الجسر الشمالي والجنوبي والكروت التي تركب على الحاسب، يتم عملها من البيوس ومن ثم توصيلها لنظام التشغيل المستخدم على الحاسب مثل ويندوز وغيره، برامج البيوس الحديثة تعطيك القدرة على التحكم بكل إعدادات الجهاز مثل سرعة المعالج والذاكرة وتواقيتهما وحتى القدرة على التحكم بقدرة الكهرباء التي تصل إلى المكونات، برنامج البيوس يتم تخزينه بشريحة تسمى ROM وهي اختصار لجملة Read Only Memory، مسمى الشريحة يدل على إنها من أنواع الذاكرة والتي تستطيع القراءة منها فقط، هذا الكلام كان صحيحا فيما سبق وذلك للمحافظة على هذا البرنامج المهم من التلف، فيتم حمايته من الكتابة عليه حتى لا يتلف، الوضع تغير الآن مع اللوحات الحديثة، الآن باستخدام برامج متخصصة بإمكانك أن تعمل ترقية لبرنامج البيوس وذلك لحل مشاكل ربما تقع في اللوحة الأم أو إضافة دعم لمعالج جديد، عند قيامك بعمل تعديلات على البيوس مثل تعريف قطعة جديدة من العتاد أو إعدادات سرعة الناقل الأمامي وحتى تغيير التاريخ والوقت، فان هذه الإعدادات يتم حفظها بشريحة تسمى CMOS وهي رمز للمسمى العلمي Complementary Metal Oxide Semiconductor، هذه الشريحة لا تستطيع تخزين معلومات بدون طاقة كهربائية، لذا فهي مربوطة ببطارية صغيرة مهمتها تزويد هذه الشريحة بالكهرباء بصورة مستمرة. وقد ظهر في بعض اللوحات ما يمسى بالبيوس المزدوج (Dual BIOS) خاصة في لوحات أم جيجابايت، في الحقيقة البيوس المزدوج تعطي مجال أكبر للمستخدمين لترقية وتعديل البيوس بدون أي خطورة تذكر أو خوف، فعندما يحدث خلل أو خطأ أثناء ترقية البيوس، سيعطي البيوس المزدوج فرصة لإعادة النسخة الأصلية للبيوس بدون أي مشكلة، وإذا حدث هذه الخلل أو الخطأ في لوحة أم ليس بها البيوس المزدوج فسيكون الحل هو إعادة اللوحة الأم إلى المصنع أو إعادة برمجة البيوس عبر فني محترف .
              </p>
